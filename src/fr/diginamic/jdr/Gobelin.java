package fr.diginamic.jdr;

public class Gobelin {

	private int force;
	private int pdv;

	public Gobelin(int force, int pdv) {
		super();
		this.force = force;
		this.pdv = pdv;
	}

	public int getForce() {
		return force;
	}

	public void setForce(int force) {
		this.force = force;
	}

	public int getPdv() {
		return pdv;
	}

	public void setPdv(int pdv) {
		this.pdv = pdv;
	}

}
